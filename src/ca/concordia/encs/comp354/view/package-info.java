package ca.concordia.encs.comp354.view;

/**
*
* @author Christopher Idah
* 
* This is the package level documentation for the {@link ca.concordia.encs.comp354.view} package.
* 
* It is part of the MVC design pattern used to implement the Codenames board game.
* 
* The function of the classes in this package is to provide a user friendly visualization of the game data that the objects from the 
* model package contain before, during and after the actual game is running. 
* 
* This is done by incorporating the logic and control flow classes from the {@link ca.concordia.encs.comp354.controller} package
* and updating the visual representations of the data the {@link ca.concordia.encs.comp354.model}
* package that handles the visualization of data through the game.
* 
* This allows for a higher degree of encapsulation as it keeps the model and
* view packages separate from each other.
*/